"""Idea Quantity Drill — отключение внутреннего критика."""
import random

EXERCISE_TYPE = "quantity"

_BRIEFS = [
    "моушен-заставка для стартапа без бюджета",
    "3D-продуктовый ролик для клиента который «сам не знает что хочет»",
    "анимация для корпоративного мероприятия которое никто не хочет посещать",
    "брендинг для нового локального кофе",
    "интро для подкаста о технологиях",
    "баннер для рекламной кампании без чёткого CTA",
    "концепция визуального стиля для HR-бренда",
    "идеи как объяснить клиенту что дешевле не значит лучше",
]

_GOAL_BY_LEVEL = {
    1: "10 идей",
    2: "15 идей",
    3: "20 идей",
    4: "25 идей",
}


def get_exercise(level: int, seed: int = 0) -> dict:
    rng = random.Random(seed)
    brief = rng.choice(_BRIEFS)
    goal = _GOAL_BY_LEVEL.get(level, "10 идей")
    prompt = (
        f"*Дрель количества — уровень {level}*\n\n"
        f"Бриф: _{brief}_\n\n"
        f"Цель: *{goal}*. Подряд. Без пауз. Без самоцензуры.\n\n"
        "Плохие идеи — норм. Абсурдные — лучше. "
        "Внутренний критик — в угол.\n\n"
        "_Пиши списком, не думай долго над каждой._"
    )
    return {"type": EXERCISE_TYPE, "level": level, "prompt": prompt}
