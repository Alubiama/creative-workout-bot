"""Remote Associates Task — ассоциативность."""
import random

EXERCISE_TYPE = "rat"

_TRIPLETS_BY_LEVEL: dict[int, list[tuple[str, str, str]]] = {
    1: [
        ("ВРЕМЯ", "МАШИНА", "НАЗАД"),
        ("ЧЁРНЫЙ", "РЫНОК", "СПИСОК"),
        ("ГОРЯЧИЙ", "СОБАКА", "ТОЧКА"),
        ("ОСТРЫЙ", "УГОЛ", "ВЗГЛЯД"),
        ("СВЕТ", "МОСТ", "ВРЕМЯ"),
    ],
    2: [
        ("ИДЕЯ", "СЕТЬ", "УЗЕЛ"),
        ("ТЕНЬ", "ГОЛОС", "СЛЕД"),
        ("СЛОМ", "ОБРАЗ", "РИТМ"),
        ("ФОРМА", "ПУСТОТА", "СМЫСЛ"),
        ("РАМКА", "ГРАНИЦА", "ЦЕНТР"),
    ],
    3: [
        ("ПИКСЕЛЬ", "ДЕДЛАЙН", "СЛОЙ"),
        ("РЕНДЕР", "ТЕМА", "ПОТОК"),
        ("МАСКА", "АНИМАЦИЯ", "КОРЕНЬ"),
        ("КАДР", "ПАУЗA", "ДВИЖЕНИЕ"),
        ("СЕТКА", "РИТМ", "СДВИГ"),
    ],
    4: [
        ("ФРЕЙМВОРК", "ТИШИНА", "ВЫХОД"),
        ("ИТЕРАЦИЯ", "ИНТУИЦИЯ", "ПЕТЛЯ"),
        ("КОНЦЕПТ", "ШРИФТ", "РАЗРЫВ"),
    ],
}


def get_exercise(level: int, seed: int = 0) -> dict:
    rng = random.Random(seed)
    triplets = _TRIPLETS_BY_LEVEL.get(level, _TRIPLETS_BY_LEVEL[1])
    w1, w2, w3 = rng.choice(triplets)
    prompt = (
        f"*Удалённые ассоциации — уровень {level}*\n\n"
        f"Три слова: *{w1} / {w2} / {w3}*\n\n"
        "Найди одно слово, которое связывает все три.\n"
        "_Просто одно слово. Можно с коротким объяснением._"
    )
    return {"type": EXERCISE_TYPE, "level": level, "prompt": prompt}
