"""Constraint Thinking — мышление в ограничениях."""
import random

EXERCISE_TYPE = "constraints"

_TASKS = [
    "Придумай концепцию рекламной кампании для нового моушен-проекта",
    "Опиши идею для заставки корпоративного видео",
    "Предложи концепцию бренда для небольшой студии",
    "Придумай идею для персонажной анимации без диалогов",
    "Опиши концепцию интро для YouTube-канала о технологиях",
    "Придумай идею для 3D-визуализации продукта",
]

_CONSTRAINTS_SINGLE = [
    "только 2 слова в описании",
    "без использования цвета — только форма и движение",
    "за 60 секунд — пиши первое что приходит",
    "используя только то что есть в твоей комнате прямо сейчас",
    "для аудитории старше 70 лет",
    "для аудитории из другой страны, которая не знает твой язык",
    "без бюджета вообще",
    "используя только один объект",
]

_CONSTRAINTS_DOUBLE = [
    ("только 2 слова", "без цвета"),
    ("за 60 секунд", "для аудитории другой культуры"),
    ("без бюджета", "только движение без текста"),
    ("для детей 5-7 лет", "максимально абстрактно"),
]

_CONSTRAINTS_TRIPLE = [
    ("только 2 слова", "без цвета", "за 45 секунд"),
    ("без бюджета", "для аудитории 70+", "только звук без картинки"),
]


def get_exercise(level: int, seed: int = 0) -> dict:
    rng = random.Random(seed)
    task = rng.choice(_TASKS)

    if level == 1:
        c = rng.choice(_CONSTRAINTS_SINGLE)
        constraints_text = f"Ограничение: *{c}*"
    elif level == 2:
        a, b = rng.choice(_CONSTRAINTS_DOUBLE)
        constraints_text = f"Ограничения: *{a}* и *{b}*"
    elif level >= 3:
        a, b, c = rng.choice(_CONSTRAINTS_TRIPLE)
        constraints_text = f"Три ограничения одновременно: *{a}*, *{b}*, *{c}*"
    else:
        constraints_text = f"Ограничение: *{rng.choice(_CONSTRAINTS_SINGLE)}*"

    prompt = (
        f"*Мышление в ограничениях — уровень {level}*\n\n"
        f"Задача: {task}\n\n"
        f"{constraints_text}\n\n"
        "_Не игнорируй ограничение. Оно — главное условие._"
    )
    return {"type": EXERCISE_TYPE, "level": level, "prompt": prompt}
