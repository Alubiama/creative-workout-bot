"""TRIZ principles — инженерное творческое мышление."""
import random

EXERCISE_TYPE = "triz"

_PRINCIPLES = [
    {
        "name": "Дробление",
        "description": "Раздели объект или систему на независимые части",
        "prompt_template": (
            "Примени принцип *Дробления* к задаче:\n\n"
            "_{task}_\n\n"
            "Что можно разбить на части? Какие части получатся? "
            "Как это улучшит решение?"
        ),
    },
    {
        "name": "Идеальное конечное решение",
        "description": "Система выполняет функцию сама, без дополнительных ресурсов",
        "prompt_template": (
            "Примени принцип *Идеального конечного решения* к задаче:\n\n"
            "_{task}_\n\n"
            "Опиши идеальный вариант где система «делает сама себя». "
            "Что для этого нужно убрать, а не добавить?"
        ),
    },
    {
        "name": "Инверсия",
        "description": "Сделай противоположное тому, что диктует логика",
        "prompt_template": (
            "Примени принцип *Инверсии* к задаче:\n\n"
            "_{task}_\n\n"
            "Переверни условие. Что если делать всё наоборот? "
            "Где это даёт неожиданное преимущество?"
        ),
    },
    {
        "name": "Совмещение несовместимого",
        "description": "Объедини объекты или функции, которые кажутся несовместимыми",
        "prompt_template": (
            "Примени принцип *Совмещения несовместимого* к задаче:\n\n"
            "_{task}_\n\n"
            "Что две противоположные вещи дадут если их объединить? "
            "Ищи противоречие и используй его как ресурс."
        ),
    },
]

_TASKS = [
    "клиент хочет видео «дорого и серьёзно», но бюджет минимальный",
    "нужно объяснить сложный продукт за 15 секунд",
    "анимация должна быть одновременно спокойной и энергичной",
    "логотип должен работать на чёрном и на белом фоне без изменений",
    "нужно сделать презентацию которую запомнят, но без лишнего шума",
    "клиент хочет «уникально», но образцы из референсов — банальные",
]


def get_exercise(level: int, seed: int = 0) -> dict:
    import random as _r
    rng = _r.Random(seed)
    principle = rng.choice(_PRINCIPLES)
    task = rng.choice(_TASKS)
    prompt_body = principle["prompt_template"].format(task=task)
    prompt = f"*ТРИЗ — уровень {level}*\n\n{prompt_body}"
    return {"type": EXERCISE_TYPE, "level": level, "prompt": prompt}
