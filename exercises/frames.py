"""Frame Mixing — смешение фреймов из разных областей."""
import random

EXERCISE_TYPE = "frames"

_CONTEXT_PAIRS_BY_LEVEL: dict[int, list[tuple[str, str]]] = {
    1: [
        ("кулинарный рецепт", "дизайн логотипа"),
        ("армейская операция", "утренний брифинг"),
        ("садоводство", "разработка бренда"),
        ("строительство дома", "создание анимации"),
        ("скорая помощь", "правки от клиента"),
    ],
    2: [
        ("архитектура японского сада", "agile-дедлайн в агентстве"),
        ("джазовая импровизация", "корпоративная презентация"),
        ("антарктическая экспедиция", "фриланс-проект"),
        ("дипломатические переговоры", "согласование правок"),
    ],
    3: [
        ("теория игр", "ценообразование в дизайне"),
        ("эволюционная биология", "итерации продукта"),
        ("военная стратегия", "построение портфолио"),
        ("буддийская медитация", "работа с тревогой на сдаче проекта"),
    ],
    4: [
        ("квантовая неопределённость", "творческий выбор концепции"),
        ("теория систем", "коммуникация с трудным клиентом"),
        ("нейроархитектура", "дизайн пользовательского опыта"),
    ],
}


def get_exercise(level: int, seed: int = 0) -> dict:
    rng = random.Random(seed)
    pairs = _CONTEXT_PAIRS_BY_LEVEL.get(level, _CONTEXT_PAIRS_BY_LEVEL[1])
    ctx_a, ctx_b = rng.choice(pairs)
    prompt = (
        f"*Смешение фреймов — уровень {level}*\n\n"
        f"Два контекста: *{ctx_a}* и *{ctx_b}*\n\n"
        "Опиши что получится если смешать логику, язык и подход одного "
        "в рамках другого. Какие принципы переносятся? Что ломается? "
        "Что неожиданно работает?\n\n"
        "_Один абзац. Конкретно._"
    )
    return {"type": EXERCISE_TYPE, "level": level, "prompt": prompt}
