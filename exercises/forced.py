"""Forced Connections — аналогическое мышление."""
import random

EXERCISE_TYPE = "forced"

_PAIRS_BY_LEVEL: dict[int, list[tuple[str, str]]] = {
    1: [
        ("холодильник", "библиотека"),
        ("маяк", "будильник"),
        ("паутина", "метро"),
        ("зеркало", "экзамен"),
        ("корень дерева", "пароль"),
    ],
    2: [
        ("энтропия", "меню"),
        ("молчание", "дедлайн"),
        ("граница", "вдохновение"),
        ("петля", "репутация"),
        ("эхо", "бренд"),
    ],
    3: [
        ("японский сад", "agile-спринт"),
        ("архитектура собора", "UI-система"),
        ("джазовая импровизация", "питч клиенту"),
        ("геология", "карьерный рост"),
        ("полярная навигация", "стратегия бренда"),
    ],
    4: [
        ("принцип рычага", "сторителлинг"),
        ("теория хаоса", "дизайн-система"),
        ("феромоны", "виральность"),
    ],
}


def get_exercise(level: int, seed: int = 0) -> dict:
    rng = random.Random(seed)
    pairs = _PAIRS_BY_LEVEL.get(level, _PAIRS_BY_LEVEL[1])
    a, b = rng.choice(pairs)
    prompt = (
        f"*Вынужденные связи — уровень {level}*\n\n"
        f"Два объекта: *{a}* и *{b}*\n\n"
        "Найди концепцию, которая их связывает. "
        "Не буквально — ищи структурное, метафорическое, функциональное сходство.\n\n"
        "_Один абзац. Чем неочевиднее — тем лучше._"
    )
    return {"type": EXERCISE_TYPE, "level": level, "prompt": prompt}
