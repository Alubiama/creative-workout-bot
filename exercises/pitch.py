"""Pitch — вербализация и продажа идеи."""
import random

EXERCISE_TYPE = "pitch"

_RAW_IDEAS = [
    "анимация которая реагирует на голос клиента",
    "логотип который меняется в зависимости от времени года",
    "3D-продуктовый ролик где продукт никогда не показан полностью",
    "корпоративное видео без людей — только объекты и движение",
    "бренд-идентика основанная на звуке а не на форме",
    "моушен-заставка которая генерируется процедурно каждый раз",
    "инфографика которая «живёт» и обновляется в реальном времени",
]

_AUDIENCES = {
    4: [
        "директору завода который не знает что такое моушен",
        "бабушке которая никогда не видела видеорекламу",
        "ребёнку 8 лет",
    ]
}

_FORMAT_BY_LEVEL = {
    1: "Объясни идею без ограничений. Как угодно.",
    2: "Объясни за *3 предложения*: [Что это] + [Почему это работает] + [Что получит клиент]",
    3: "Объясни за *одно предложение*. Одно.",
    4: "Объясни {audience}. За 3 предложения, понятных именно этой аудитории.",
}


def get_exercise(level: int, seed: int = 0) -> dict:
    rng = random.Random(seed)
    idea = rng.choice(_RAW_IDEAS)
    format_instruction = _FORMAT_BY_LEVEL.get(level, _FORMAT_BY_LEVEL[2])

    if level == 4:
        audience = rng.choice(_AUDIENCES[4])
        format_instruction = format_instruction.format(audience=audience)

    prompt = (
        f"*Питч идеи — уровень {level}*\n\n"
        f"Сырая идея: _{idea}_\n\n"
        f"{format_instruction}\n\n"
        "_Не «объясняй концепцию». Продавай._"
    )
    return {"type": EXERCISE_TYPE, "level": level, "prompt": prompt}
