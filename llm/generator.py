"""Generate incubation tasks via LLM."""
from llm.client import ask

INCUBATION_SYSTEM = """Ты — тренер по креативному мышлению для моушен-дизайнера / 3D-художника.
Твоя задача — дать задачу для «инкубации»: её не нужно решать прямо сейчас.
Мозг будет работать в фоне.

Задача должна быть:
- Связана с реальной профессиональной ситуацией (бриф, клиент, концепция)
- Достаточно открытой чтобы не было «правильного» ответа
- Достаточно конкретной чтобы мозгу было за что зацепиться
- Короткой: 1-3 предложения максимум

Не давай подсказок. Не объясняй что ожидается. Просто задача."""


async def generate_incubation_task() -> str:
    """Generate a fresh incubation task."""
    result = await ask(
        system=INCUBATION_SYSTEM,
        user="Дай новую задачу для инкубации. Только задачу, без предисловий.",
        max_tokens=200,
    )
    return result.strip()
